#include <iostream>
using namespace std;

int main() {
	// -=-=- Texto separado -=-=-

	// Versão com endl
	cout << "Bem-vindo" << endl;
	cout << "ao mundo" << endl;
	cout << "do C++" << endl;
	cout << endl;

	// Versão com \n
	cout << "Bem-vindo \n";
	cout << "ao mundo \n";
	cout << "do C++ \n";
	cout << "\n";

	// -=-=- Texto junto -=-=-

	cout << "Bem-vindo ";
	cout << "ao mundo ";
	cout << "do C++";
}